package Modelo;

public class Orden {

	private int numeroorden;

	public Orden() {
		super();
	}

	public Orden(int numeroorden) {
		super();
		this.numeroorden = numeroorden;
	}

	public int getNumeroorden() {
		return numeroorden;
	}

	public void setNumeroorden(int numeroorden) {
		this.numeroorden = numeroorden;
	}
}
